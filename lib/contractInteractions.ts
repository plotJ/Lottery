let tokenContract: ethers.Contract;

export async function connectWallet() {
  if (typeof window.ethereum !== 'undefined') {
    try {
      await window.ethereum.request({ method: 'eth_requestAccounts' });
      provider = new ethers.providers.Web3Provider(window.ethereum);
      signer = provider.getSigner();
      lotteryContract = new ethers.Contract(LOTTERY_ADDRESS, LOTTERY_ABI, signer);
      tokenContract = new ethers.Contract(TOKEN_ADDRESS, TOKEN_ABI, signer);
      return true;
    } catch (error) {
      console.error('Failed to connect wallet:', error);
      return false;
    }
  } else {
    console.log('Please install MetaMask!');
    return false;
  }
}

export async function getLotteryStatus() {
  const betsOpen = await lotteryContract.betsOpen();
  const closingTime = await lotteryContract.closingTime();
  return { betsOpen, closingTime: new Date(closingTime.toNumber() * 1000) };
}

export async function buyTokens(ethAmount: string) {
  const tx = await lotteryContract.purchaseTokens({ value: ethers.utils.parseEther(ethAmount) });
  await tx.wait();
}

export async function placeBet() {
  const tx = await lotteryContract.bet();
  await tx.wait();
}

export async function openBets(duration: number) {
  const tx = await lotteryContract.openBets(duration);
  await tx.wait();
}

export async function closeLottery() {
  const tx = await lotteryContract.closeLottery();
  await tx.wait();
}

export async function claimPrize() {
  const prize = await lotteryContract.prize(await signer.getAddress());
  if (prize.gt(0)) {
    const tx = await lotteryContract.prizeWithdraw(prize);
    await tx.wait();
  }
}

export async function getTokenBalance() {
  const address = await signer.getAddress();
  return tokenContract.balanceOf(address);
}

export async function burnTokens(amount: string) {
  const tx = await tokenContract.burn(ethers.utils.parseEther(amount));
  await tx.wait();
}